#              Concatenação (+) e repetição (*) com operadores aritméticos

concatenacao = 'A' + 'B' + 'C'
print(concatenacao)

concatenacao2 = 'Rogério' + ' ' + 'Ferreira' # aqui criei espaços entre o nome.
print(concatenacao2)

a_dez_vezes = 'A' * 10 # repetição de valores 
tres_vezes_luiz = 3 *  ' '  'Luiz'
print(a_dez_vezes)
print(tres_vezes_luiz)